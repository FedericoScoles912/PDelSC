function suma(a, b) {
  return a + b;
}
console.log("La suma entre 4 y 5 es:", suma(4, 5));

function resta(a, b) {
  return a - b;
}
console.log("La resta entre 3 y 6 es:", resta(3, 6));

function multipliclica(a, b) {
  return a * b;
}
console.log("La multiplicación entre 2 y 7 es:", multipliclica(2, 7));

function divide(a, b) {
  return a / b;
}
console.log("La división entre 20 y 4 es:", divide(20, 4));
